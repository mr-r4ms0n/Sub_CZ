    private void swOptAMouseClicked(java.awt.event.MouseEvent evt)//GEN-FIRST:event_swOptAMouseClicked
    {//GEN-HEADEREND:event_swOptAMouseClicked
        if (swOptA.isActivado())
        {
            swOptM.setActivado(false);
            btnConfirmar.setEnabled(true);
        } else
        {
            if (!swOptM.isActivado())
            {
                btnConfirmar.setEnabled(false);
            }
        }
    }//GEN-LAST:event_swOptAMouseClicked

