    private void btnConfirmarActionPerformed(java.awt.event.ActionEvent evt)//GEN-FIRST:event_btnConfirmarActionPerformed
    {//GEN-HEADEREND:event_btnConfirmarActionPerformed
        if (swOptA.isActivado())
        {
            bloq_unloccomp(false);
            ChConj conj = new ChConj("auto");
            conj.setLocation(1250, 396);
            conj.setVisible(true);

        } else
        {
            if (swOptM.isActivado())
            {
                bloq_unloccomp(false);
                ChConj conj = new ChConj("manual");
                conj.setLocation(1250, 396);
                conj.setVisible(true);
            }
        }
    }//GEN-LAST:event_btnConfirmarActionPerformed

    public static void bloq_unloccomp(boolean val)
    {
        lblTitulo.setEnabled(val);
        lblTitulo11.setEnabled(val);
        lblTitulo2.setEnabled(val);
        btnConfirmar.setEnabled(false);
        swOptA.setEnabled(val);
        swOptM.setEnabled(val);
        swOptA.setActivado(false);
        swOptM.setActivado(false);
        if (val==true)
        {
            vtn.setOpacity(1);
        }else
        {
            vtn.setOpacity(0.1f);
        }
        
    }

